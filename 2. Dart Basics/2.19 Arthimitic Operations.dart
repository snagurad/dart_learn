//2.19 Arthimitic Operations
void main() {
  //Arithmetic
  // + - * / ~/(Int) %
  print(5 + 2);
  print(5 - 2);
  print(5 * 2);
  print(5 / 2);
  print(5 ~/ 2);
  print(5 % 2);

  //Augmented
  // += -= *= /= ~/= %=
  int x = 5;
  x *= 2;
  print(x);
  
  //postfix increment decrement
  // ++ --
  x = 0;
  x++;
  print(x);
  ++x;
  print(x);
  
}
